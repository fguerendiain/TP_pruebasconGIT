long suma(long operador1, long operador2){

    long resultado;
    resultado = operador1 + operador2;

    return resultado;
}

long resta(long operador1, long operador2){

    long resultado;
    resultado = operador1 - operador2;

    return resultado;
}

long multiplicacion(long operador1, long operador2){

    long resultado;
    resultado = operador1 * operador2;

    return resultado;
}

long division(long operador1, long operador2){

    long resultado;
    resultado = operador1 / operador2;

    return resultado;
}


